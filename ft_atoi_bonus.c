#include "push_swap_bonus.h"

long	ft_atoi(char *str)
{
	int		i;
	int		s;
	long	r;

	i = 0;
	while (str[i] == ' ' || str[i] == '\t')
		i++;
	s = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	r = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		if ((r > 2147483647 && s == 1) || (r > 2147483648 && s == -1))
			return (write(2, "Error\n", 6), 2147483648);
		i++;
	}
	r = r * s;
	return (r);
}
